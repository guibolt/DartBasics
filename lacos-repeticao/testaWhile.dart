void main(){
  int contador = 0;
  
  print("Comecando com o while");
  while( contador <=10){
    print("O valor do contador é $contador");
    contador++;
  }
  contador = 0;

  print("Agora vamos para o do While!");
  do {
     print("O valor do contador é $contador");
    contador++;
    
  } while (contador <=10);

}